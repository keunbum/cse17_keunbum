const int N = 10;

bool chk[N + N];

int main() {
  ios::sync_with_stdio(false);
  cin.tie(0);
  int n;
  cin >> n;
  vector<vector<int>> a(n, vector<int>(n));
  for (int i = 0; i < n; i++) {
    for (int j = 0; j < n; j++) {
      cin >> a[i][j];
    }
  }
  function<int(int)> Dfs = [&](int s) {
    if (s > n + n - 1) {
      return 0;
    }
    int res = -1;
    for (int x = min(s, n - 1), y = s - x; x >= 0 && y <= n - 1; x--, y++) {
      if (a[x][y] == 1 && !chk[x - y + n - 1]) {
        chk[x - y + n - 1] = true;
        res = max(res, Dfs(s + 2) + 1); 
        chk[x - y + n - 1] = false;
      }
    }
    if (res == -1) {
      res = Dfs(s + 2);
    }
    return res;
  };
  cout << Dfs(0) + Dfs(1) << '\n';
  return 0;
}
